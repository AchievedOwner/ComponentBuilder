namespace ComponentBuilder.Definitions;

/// <summary>
/// The HTML type of button.
/// </summary>
public enum ButtonHtmlType
{
    /// <summary>
    /// A normal button.
    /// </summary>
    Button,
    /// <summary>
    /// A submit button can trigger form onsubmit event.
    /// </summary>
    Submit,
    /// <summary>
    /// Reset and clear the value of input controls in form.
    /// </summary>
    Reset,
}
